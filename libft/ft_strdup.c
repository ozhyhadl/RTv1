#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*tmp;

	i = 0;
	if ((tmp = (char *)malloc(ft_strlen(s1) + 1)) == NULL)
		return (NULL);
	tmp = ft_strcpy(tmp, s1);
	return (tmp);
}
