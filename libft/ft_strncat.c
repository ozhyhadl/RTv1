#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t z;

	i = 0;
	z = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[z] != '\0' && z < n)
	{
		s1[i] = s2[z];
		z++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
